// Function to create list items for each item in an object
function createListItems(containerId, dataObject) {
    const container = document.getElementById(containerId);
    for (const itemName in dataObject) {
      if (dataObject.hasOwnProperty(itemName)) {
        const items = dataObject[itemName];
        for (let i = 0; i < items.length; i++) {
          const itemData = items[i];
          const listItem = document.createElement("li");
          const link = document.createElement("a");
          link.href = itemData.link;
          link.textContent = itemData.host + ": " + itemData.name + (items.length > 1 ? ` (${i + 1})` : '');
          listItem.appendChild(link);
          container.appendChild(listItem);
        }
      }
    }
  }
  
